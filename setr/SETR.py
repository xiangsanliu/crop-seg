from setr.encoder import Encoder
from setr.decoder import Decoder
import torch.nn as nn

class SETRModel(nn.Module):
    def __init__(self, config):
        super().__init__()
        self.encoder = Encoder(config)
        self.decoder = Decoder(config)

    def forward(self, x):
        x = self.encoder(x)
        x = self.decoder(x)
        return x
